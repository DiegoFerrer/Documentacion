# Modulos de terceros PIP | https://pypi.org/project/pip/

'''  # PIPenv | Administrador de paquetes, parecido a lo que es NPM
Es una herramienta para iniciar proyecto, controlar dependencias y tener un entorno virtual '''

# Instalar
'''
1. pip install pipenv
2. pipenv shell | para crear un nuevo entorno virtual en el proyecto seleccionado (carpeta)
3. Instalar modulos como dependencias o dev dependencias
    A. pipenv install nameModulo | pipenv install nameModulo --dev
4. pipenv lock -r | para ver los modulos que tengo instalados
5. pipenv --rm | para remover el entorno virtual
''' 


